# 字面量的写法
print(100) # 整数字面量 (int)
print(3.14) # 浮点数字面量 (float)
print("Hello Python") # 字符串字面量 (str)
print(True) # 布尔字面量 (bool)
print(None) # 空值字面量 (NoneType)
print("------------")

# 布尔类型本质也是整数类型 （Ture == 1, False == 0）
print(True + 1) # True在运算中等同于1

# 变量的定义与使用, 变量就是用来存储数据的容器
# Python是动态类型语言，一个变量是可以存储任意类型的数据（但是在项目中，推荐变量只存储一种类型的数据）
num = 1113.1

num = num + 2
print(num)

num =  "OK"
print(num)

num = True
print(num)

